        3. ÚLCERAS DE LA PIEL POR PRESIÓN (UPP)
        ASPECTO CONTENIDO Y RECOMENDACIONES
        1. ¿En qué
        consiste?
        Las úlceras por presión son lesiones o heridas de la piel que se producen
        en personas con los siguientes riesgos:
         Poca movilidad, encamados, incontinencia, mala nutrición o
        enfermedades crónicas muy limitantes, entre otras).
        Una úlcera por presión se forma en una persona con riesgo, cuando una
        zona de la piel permanece presionada durante varias horas por el peso del
        cuerpo, impidiendo la circulación de la sangre y produciendo la muerte de
        las diferentes capas de la piel.
        Por esta razón las zonas más frecuentes para que se formen las úlceras,
        son talón, zona lateral de la pelvis, zona posterior de la cadera (sacro),
        espalda, parte posterior de la cabeza, orejas, etc.
        Cuando se forman las úlceras, hay varios grados en su evolución:
         Grado I: Es la fase inicial, cuando hay una zona de la piel con
        enrojecimiento, que se mantiene tras el cambio de postura (se
        denomina eritema).
         Grado II: Se afecta la capa superficial de la piel, en la que se observa
        un abultamiento con forma de bolsa pequeña de líquido (se
        denomina vesícula), que se parece a las ampollas de las
        quemaduras leves) o herida.
         Grado III: Se observa una herida abierta de la piel, en la que se
        afecta toda la piel, la grasa y el músculo. En fase, dicha herida
        puede estar cubierta por una costra.
         Grado IV: La herida ya es muy profunda, afecta músculo y hueso.
