        5. NUTRICIÓN E HIDRATACIÓN
        ASPECTO CONTENIDO Y RECOMENDACIONES
        1. ¿En qué consiste?
        La nutrición es el conjunto de procesos que permite al organismo
        utilizar y transformar los nutrientes para mantenerse vivo.
        La alimentación es la manera de proporcionar al organismo los
        nutrientes que necesita (proteínas, hidratos de carbono, grasas,
        vitaminas, minerales y agua). La alimentación es voluntaria y
        educable.
        La hidratación es la forma de restablecer el grado de humedad de
        la piel u otros tejidos por medio del agua.
